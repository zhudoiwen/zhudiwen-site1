#!/usr/bin/env node
// Build script for Cloudflare Pages
// Runs next build first, then copies CSS files (Next.js 16 with Turbopack puts CSS in chunks/ instead of css/),
// then runs @opennextjs/cloudflare build, then copies CSS to the final output

import { execSync } from 'child_process';
import fs from 'fs';
import path from 'path';

const cwd = process.cwd();

console.log('=== Step 1: Building Next.js app ===');
execSync('bun next build', { stdio: 'inherit', cwd });

console.log('\n=== Step 2: Pre-copying CSS files for OpenNext compatibility ===');
// Copy CSS to standalone BEFORE OpenNext build, so copyTracedFiles can find it
const sourceStaticDir = path.join(cwd, '.next', 'static');
const standaloneStaticDir = path.join(cwd, '.next', 'standalone', '.next', 'static');

const cssDir = path.join(standaloneStaticDir, 'css');
if (!fs.existsSync(cssDir)) {
  fs.mkdirSync(cssDir, { recursive: true });
}

const chunksDir = path.join(sourceStaticDir, 'chunks');
if (fs.existsSync(chunksDir)) {
  const files = fs.readdirSync(chunksDir);
  for (const file of files) {
    if (file.endsWith('.css')) {
      fs.copyFileSync(
        path.join(chunksDir, file),
        path.join(cssDir, file)
      );
      console.log(`  Copied ${file} to .next/standalone/.next/static/css/`);
    }
  }
}

console.log('\n=== Step 3: Running OpenNext Cloudflare build ===');
execSync('bunx @opennextjs/cloudflare build', { stdio: 'inherit', cwd });

console.log('\n=== Step 4: Renaming worker.js to _worker.js for Cloudflare Pages ===');
const workerJsPath = path.join(cwd, '.open-next', 'worker.js');
const underscoreWorkerJsPath = path.join(cwd, '.open-next', '_worker.js');
if (fs.existsSync(workerJsPath)) {
  fs.renameSync(workerJsPath, underscoreWorkerJsPath);
  console.log('  Renamed worker.js -> _worker.js');
}

console.log('\n=== Step 5: Post-copying CSS files to final output ===');
// After OpenNext build, copy CSS to the final .open-next/assets directory
const openNextAssetsStaticDir = path.join(cwd, '.open-next', 'assets', '_next', 'static');
const openNextCssDir = path.join(openNextAssetsStaticDir, 'css');
if (!fs.existsSync(openNextCssDir)) {
  fs.mkdirSync(openNextCssDir, { recursive: true });
}

// Copy CSS from chunks to css in the final output
const openNextChunksDir = path.join(openNextAssetsStaticDir, 'chunks');
if (fs.existsSync(openNextChunksDir)) {
  const files = fs.readdirSync(openNextChunksDir);
  for (const file of files) {
    if (file.endsWith('.css')) {
      fs.copyFileSync(
        path.join(openNextChunksDir, file),
        path.join(openNextCssDir, file)
      );
      console.log(`  Copied ${file} to .open-next/assets/_next/static/css/`);
    }
  }
}

console.log('\n=== Step 6: Copying SSG pre-rendered HTML pages to assets ===');
// OpenNext Cloudflare adapter does not copy SSG HTML files to assets.
// We need to manually copy them so Cloudflare Pages can serve them directly.
const nextServerAppDir = path.join(cwd, '.next', 'server', 'app');
const assetsDir = path.join(cwd, '.open-next', 'assets');

function copySsgPages(dir, relativePath = '') {
  if (!fs.existsSync(dir)) return;
  
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    const relPath = relativePath ? `${relativePath}/${entry.name}` : entry.name;
    
    if (entry.isDirectory()) {
      // Skip special directories
      if (entry.name === 'page' || entry.name.startsWith('_') || entry.name === 'opengraph-image') continue;
      copySsgPages(fullPath, relPath);
    } else if (entry.isFile() && entry.name.endsWith('.html')) {
      // This is a SSG pre-rendered HTML page
      // The file is named like "api-design-principles.html" but should be served at /blog/api-design-principles
      // We need to create a directory with index.html
      const pageName = entry.name.replace(/\.html$/, '');
      const targetDir = path.join(assetsDir, relativePath, pageName);
      const targetFile = path.join(targetDir, 'index.html');
      
      if (!fs.existsSync(targetDir)) {
        fs.mkdirSync(targetDir, { recursive: true });
      }
      fs.copyFileSync(fullPath, targetFile);
      console.log(`  Copied SSG page: ${relPath} -> ${relativePath}/${pageName}/index.html`);
    }
  }
}

// Copy SSG pages from .next/server/app to .open-next/assets
copySsgPages(nextServerAppDir);

// Also copy the root index.html
const rootIndexHtml = path.join(nextServerAppDir, 'index.html');
const rootIndexTarget = path.join(assetsDir, 'index.html');
if (fs.existsSync(rootIndexHtml)) {
  fs.copyFileSync(rootIndexHtml, rootIndexTarget);
  console.log('  Copied root index.html');
}

console.log('\n=== Step 7: Patching _worker.js to serve static HTML assets ===');
// The _worker.js generated by OpenNext does not serve static HTML files.
// We need to add logic to serve static HTML files from assets before falling back to the Next.js server.
const workerPath = path.join(cwd, '.open-next', '_worker.js');
let workerContent = fs.readFileSync(workerPath, 'utf-8');

// Add static asset serving logic before the middleware handler call
// In Cloudflare Pages, env.ASSETS is available to access static files
// We need to use a different approach: check if the request path matches a known SSG page
// and if so, try to serve it from assets
const staticAssetCheck = `
            // Try to serve static HTML files from assets first
            // This handles SSG pre-rendered pages that were copied to assets
            // Only try for paths that don't have a file extension (i.e., page routes)
            if (!url.pathname.includes('.')) {
                try {
                    // Build the asset path: /blog/api-design-principles -> /blog/api-design-principles/index.html
                    let assetPath = url.pathname;
                    if (assetPath === '/' || assetPath === '') {
                        assetPath = '/index.html';
                    } else {
                        assetPath = assetPath.endsWith('/') ? assetPath + 'index.html' : assetPath + '/index.html';
                    }
                    
                    // Use env.ASSETS.fetch with a Request object
                    const assetRequest = new Request('https://fake-host' + assetPath, {
                        method: 'GET',
                        headers: request.headers,
                    });
                    const assetResponse = await env.ASSETS.fetch(assetRequest);
                    if (assetResponse.status === 200) {
                        const headers = new Headers(assetResponse.headers);
                        if (!headers.has('Content-Type')) {
                            headers.set('Content-Type', 'text/html');
                        }
                        return new Response(assetResponse.body, {
                            status: 200,
                            headers,
                        });
                    }
                } catch (e) {
                    // ASSETS.fetch failed, fall through to middleware
                }
            }
`;

// Insert the static asset check before the middleware handler call
workerContent = workerContent.replace(
  'const reqOrResp = await middlewareHandler(request, env, ctx);',
  staticAssetCheck + '\n            const reqOrResp = await middlewareHandler(request, env, ctx);'
);

fs.writeFileSync(workerPath, workerContent);
console.log('  Patched _worker.js to serve static HTML assets first');

console.log('\n=== Build completed successfully! ===');